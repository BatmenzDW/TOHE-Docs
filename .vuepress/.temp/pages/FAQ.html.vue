<summary><b><font color=gray>Why did I get a pop-up saying <font color=#996d6d>"You have been kicked by room"</font>?</font></b></summary>
<p>This issue may occur because the room prohibits sending certain messages, such as saying &quot;start&quot; or other variants such as &quot;begin&quot;. Some other causes could be that you weren't logged in, used banned words, had a banned nickname, have been banned before, or you could just be on a blacklisted device, (Check <RouteLink to="/options/Settings/System.html">System Settings</RouteLink>). It may also be caused by EAC anti-cheat. If none of the above is true, then you were probably just kicked out manually by the host, or got vote-kicked by other players. Another reason you may get kicked is just connection issues.</p>
</details>
<details>
<summary><b><font color=gray>Why didn't TOHE load and give the pop-up <font color=#996d6d>"Fatal Error in GC: Collecting from unknown thread"</font>?</font></b></summary>
<p>This can randomly happen due to errors in the code. re-installing TOHE + re-pasting your Vanilla Among Us files will allow you to play again. If this happens constantly, please be patient as Developers are aware of this error. PS: Make sure you copy important files before re-installing (such as files in your Language folder, TOHE-Data folder, etc).</p>
</details>
